# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз


listFruit = ["яблоко", "банан", "киви", "арбуз", "апельсин", "виноград", "слива", "манго", "манго"]
for num, itm in enumerate(listFruit, 1):
    print('{0}. {1:>11}'.format(num, itm))

